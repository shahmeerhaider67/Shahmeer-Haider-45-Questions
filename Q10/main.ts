// Description: This program converts a temperature in Celsius to Fahrenheit.

// Define the temperature in Celsius
const celsiusTemperature: number = 25;

// Convert Celsius to Fahrenheit
const fahrenheitTemperature: number = (celsiusTemperature * 9/5) + 32;

// Print the result
console.log(`${celsiusTemperature}°C is equal to ${fahrenheitTemperature}°F.`);
