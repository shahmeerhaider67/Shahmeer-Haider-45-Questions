// Description: This program converts a temperature in Celsius to Fahrenheit.
// Define the temperature in Celsius
var celsiusTemperature = 25;
// Convert Celsius to Fahrenheit
var fahrenheitTemperature = (celsiusTemperature * 9 / 5) + 32;
// Print the result
console.log("".concat(celsiusTemperature, "\u00B0C is equal to ").concat(fahrenheitTemperature, "\u00B0F."));
